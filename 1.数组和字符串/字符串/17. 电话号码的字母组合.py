from typing import List


class Solution:
    def letterCombinations(self, digits: str) -> List[str]:
        dic = {'2': 'abc', '3': 'def', '4': 'ghi', '5': 'jkl', '6': 'mno',
               '7': 'pqrs',
               '8': 'tuv', '9': 'wxyz'}

        n = len(digits)
        idx = 0
        ans = [""]
        while idx < n:
            chars = dic[digits[idx]]
            temp_ans = []
            for char in chars:
                for a in ans:
                    temp_ans.append(a + char)
            ans = temp_ans
            idx += 1
        return ans if ans != [''] else []


